     <h1>Qu'est-ce que le rhésus du groupe sanguin ?</h1>
    <div class="row">
        <div class="col-md-4">
            <div class="imgAbt">
                <img width="350" height="350" src="/images/tube.jpg" />
            </div>
        </div> <br>  <br>
        <div class="col-md-8">
            <p align="right" >Être A+ ou A-, O+ ou O-, quelle différence ? Quelles sont les caractéristiques du sang qui déterminent notre rhésus ? Nos explications pour enfin comprendre ce que le système de dénomination rhésus signifie.

Comme dans le cas du système ABO déterminant le groupe sanguin, le système rhésus se base sur la présence ou d'absence de molécules appelées antigènes à la surface de nos globules rouges.

Le rhésus définit en fait un antigène (une molécule du système immunitaire) situé sur la paroi des globules rouges, les cellules du sang qui transportent l'oxygène. Le rhésus permet donc d'affiner la classification du sang en s'ajoutant au système ABO. Une personne est dite « rhésus positif » si ses globules rouges possèdent l'antigène D, alors qu'elle est dite « rhésus négatif » si cet antigène est absent.

Dans la population française, 85% des gens sont porteurs de cet antigène et ont donc un groupe sanguin +. Les 15% restants sont rhésus négatif.</p>
