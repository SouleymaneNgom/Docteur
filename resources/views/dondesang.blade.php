     <h1>Le Sang ?</h1>
    <div class="row">
        <div class="col-md-4">
            <div class="imgAbt">
                <img width="380" height="350" src="/images/sanguin.jpg" />
            </div>
        </div> <br>  <br>
        <div class="col-md-8">
            <p align="right" >Le sang est un tissu liquide qui circule dans notre corps grâce aux vaisseaux sanguins. Il est composé de globules rouges, de globules blancs et de plaquettes qui baignent dans un liquide appelé plasma. Le sang joue un rôle essentiel dans le transport de l’oxygène, des nutriments, des anticorps et des hormones.
Chez un adulte, le volume sanguin est d’environ 5 litres mais ce volume varie en fonction du poids, de la taille et du sexe de l’individu. 
Les globules rouges contiennent de l’hémoglobine qui donne au sang sa couleur rouge. Ils ont pour rôle de transporter l’oxygène de nos poumons vers les autres organes du corps. Les globules blancs (ou leucocytes) sont des cellules du système immunitaire qui nous protègent des agressions extérieures telles que les bactéries, les virus, les cellules étrangères, etc. Il existe trois types de globules blancs : des granulocytes, lymphocytes et monocytes qui assurent chacun, par leur spécificité propre, les fonctions de défense.</p>
