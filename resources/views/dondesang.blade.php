     <h1>Transfusions sanguines : ce que vous devez savoir</h1>
    <div class="row">
        <div class="col-md-4">
            <div class="imgAbt">
                <img width="350" height="350" src="/images/sangue.jpg" />
            </div>
        </div> <br>  <br>
        <div class="col-md-8">
            <p align="right" >Les normes en matière de sécurité des réserves de sang sont très élevées. Il existe quand même une mince chance de contracter une maladie ou d’avoir des effets nocifs après une transfusion.

Le sang donné peut parfois causer des problèmes respiratoires et faire du tort aux poumons. C’est ce que l’on appelle une grave lésion pulmonaire liée à la transfusion. Sur 5 000 unités de sang transfusé, une personne aura ce problème.

Des bactéries provenant de la peau du donneur peuvent parfois pénétrer dans le sac de sang et infecter le sang donné pendant qu’il est prélevé. Il peut en résulter une infection quand quelqu’un reçoit le sang. Sur 10 000 unités de sang transfusé, une personne aura ce problème.

Si un receveur de sang a un système immunitaire affaibli et reçoit une transfusion, les globules blancs du donneur peuvent parfois tenter de prendre le contrôle de la moelle osseuse du receveur. La nouvelle moelle peut ensuite attaquer le corps du receveur. On appelle cette condition la maladie du greffon contre l'hôte associée à la transfusion. Elle est très dangereuse, mais aussi très rare. Pour les patients chez qui l’on sait qu’ils ont un système immunitaire affaibli, comme les enfants atteints du cancer et les bébés, le laboratoire de transfusion sanguine traite (par irradiation) le sang du donneur afin de tuer les globules blancs et de prévenir ce problème.</p>
