     <h1>Les risques de la transfusion</h1>
    <div class="row">
        <div class="col-md-4">
            <div class="imgAbt">
                <img width="350" height="350" src="/images/danger.jpg" />
            </div>
        </div> <br>  <br>
        <div class="col-md-8">
            <p align="right" >Des germes Gram négatif risquent à titre exceptionnel de se développer lorsque le sang est conservé à 4 0C ; leur toxine est capable de provoquer chez le receveur un choc mortel. Les risques les plus importants sont de nature immunologique (incompatibilité érythrocytaire) et de nature infectieuse (risques devenus très faibles mais non nuls) : transmission de parasites (paludisme) ou de virus (hépatites à virus, sida, parvovirus B 19). Ces accidents ne doivent pas faire oublier que la transfusion sauve des milliers de vies humaines, et cela grâce à la générosité des donneurs bénévoles.

La transmission du sida par le sang et ses dérivés, observée dans la plupart des pays industrialisés entre les années 1980 et 1985 dans des proportions et délais comparables, a conduit à réformer nombre d'organisations transfusionnelles. En France, une Agence française du sang, devenue ensuite Établissement français du sang, avait été créée pour renforcer le contrôle des bonnes pratiques de préparation des produits sanguins et pour assurer « l'hémo-vigilance » (suivi des receveurs). La production et la distribution des produits labiles ont été disjointes de celles des produits stables , soumis à autorisation de mise sur le marché auprès de l'Agence française pour la Sécurité sanitaire et les produits de santé (Afssaps). Le fractionnement du plasma est désormais placé sous l'autorité d'un Laboratoire français des biotechnologies qui regroupe les anciens centres de fractionnement. Certains d'entre eux ont été supprimés. C'est le cas du C.N.T.S. dont les installations de fractionnement demeurent toutefois utilisées.</p>
