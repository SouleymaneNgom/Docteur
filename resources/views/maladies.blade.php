En France, environ 1 300 nouveaux cas d'hépatite A sont diagnostiqués chaque année. La transmission peut s'effectuer par contact direct entre deux personnes si l'une d'entre elles est porteuse du virus, ce qui est assez fréquent dans les crèches et dans les écoles. La transmission est dite indirecte lorsqu'elle se produit à travers la consommation d'une eau ou d'aliments infectés. </div>
