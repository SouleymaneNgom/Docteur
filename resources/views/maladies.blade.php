L’insuffisance rénale est un trouble du fonctionnement des reins. Elle est due à une destruction progressive et irrémédiable des canaux qui constituent le rein. Résultat : le sang n’est plus filtré, les déchets de l’organisme ne s’éliminent plus et s’accumulent dans l’urine. </div>
