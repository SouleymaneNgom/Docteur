On distingue plusieurs types de cancers du sein. Parmi les deux plus fréquents : le cancer non invasif qui se forme à l’intérieur des canaux de lactation du sein et le cancer invasif qui touche les tissus autour de ces canaux. Ce dernier a plus de risque de s’étendre dans le sein et à d’autres organes s’il n’est pas pris en charge rapidement. Dans tous les cas, dépisté tôt, le cancer du sein se soigne très bien et il n’est forcément nécessaire d’enlever tout le sein atteint.</div>
